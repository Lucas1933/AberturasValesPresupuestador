function calcularCosto() {
  const medida = document.getElementById("medidas").value;
  const seleccion = data.find(
    (item) => `${item.medidas.ancho}x${item.medidas.alto}` === medida
  );

  if (!seleccion) {
    document.getElementById("resultado").innerText = "Medida no encontrada.";
    return;
  }

  let costoTotal = seleccion.accesorios; // Siempre suma el costo de accesorios

  if (document.getElementById("v_entero").checked)
    costoTotal += seleccion.v_entero;
  if (document.getElementById("mm_3").checked) costoTotal += seleccion.mm_3;
  if (document.getElementById("mm_4").checked) costoTotal += seleccion.mm_4;
  if (document.getElementById("dvh").checked) costoTotal += seleccion.dvh;
  if (document.getElementById("mosquiteros").checked)
    costoTotal += seleccion.mosquiteros;
  // Sumar un 15% al costo total
  costoTotal += (costoTotal / 100) * 15;

  // Sumar un 100% adicional al resultado
  costoTotal += (costoTotal / 100) * 100;

  const costoFormateado = Math.round(costoTotal).toLocaleString("es-ES");
  document.getElementById(
    "resultado"
  ).innerText = `Costo total: $${costoFormateado}`;
}

function copiarResultado() {
  const resultado = document.getElementById("resultado").innerText;
  navigator.clipboard
    .writeText(resultado.slice(13, resultado.length))
    .then(() => {
      alert("Resultado copiado al portapapeles.");
    })
    .catch((err) => {
      alert("Error al copiar el resultado.");
    });
}
